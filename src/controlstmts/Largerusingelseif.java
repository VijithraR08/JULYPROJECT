package controlstmts;

public class Largerusingelseif {

	public static void main(String[] args) {
		int a=25,b=45,c=17;
	    if(a>b&&a>c)
		{
			System.out.println("a is larger");
		}
		else if(b>a&&b>c)
		{
			System.out.println("b is larger");
		}
		else
		{
			System.out.println("c is larger");
		}
	}

}
