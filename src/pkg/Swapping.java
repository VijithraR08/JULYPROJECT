package pkg;

public class Swapping {

	public static void main(String[] args) {
		int a=30,b=20,temp;
		temp=a;
		a=b;
		b=temp;
		System.out.println("Before swapping");
		System.out.println("a="+b + "b="+a);
		System.out.println("After swapping");
		System.out.println("a="+a + "b="+b);
	    

	}

}
